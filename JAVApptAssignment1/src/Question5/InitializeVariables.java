
/*Write a Java program to declare and initialize variables of different data types 
(int, double, String) with appropriate identifiers.*/

package Question5;

public class InitializeVariables {

	public static void main(String[] args) {
		int numb;  // declaration of numb with data type int.
		numb=12;   // initializing numb identifier with integer variable 12.
		
		double dvalue;  // declaration of dvalue with data type double.
		dvalue=12.4;   // initializing dvalue identifier with double variable 12.4.
		
		String firstname;  // declaration of firstname with data type String.
		firstname="jassica";   // initializing firstname identifier with String variable jassica.
		

	}

}
